package com.bansefi.nss.cargoabono.vo;

public class ActualizaStatusMovimientoCargoAbono 
{
	private String idMovimiento;
	private String statusOperacion;
	private String fechaContable;
	private String horaOperacion;
	private String folio;
	
	public String getIdMovimiento() {
		return idMovimiento;
	}
	public void setIdMovimiento(String idMovimiento) {
		this.idMovimiento = idMovimiento;
	}
	public String getStatusOperacion() {
		return statusOperacion;
	}
	public void setStatusOperacion(String statusOperacion) {
		this.statusOperacion = statusOperacion;
	}
	public String getFechaContable() {
		return fechaContable;
	}
	public void setFechaContable(String fechaContable) {
		this.fechaContable = fechaContable;
	}
	public String getHoraOperacion() {
		return horaOperacion;
	}
	public void setHoraOperacion(String horaOperacion) {
		this.horaOperacion = horaOperacion;
	}
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	
	
}
